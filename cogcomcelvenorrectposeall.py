# MAIN ENGINE
# ---------------------------
class Engine:
    def __init__(self):
        self.model = YOLO(MODEL_PATH)
        self.draw = Drawer()
        self.force = Force()
        self.stab = Stability()

    def _get(self, kpts, conf, i):
        return kpts[i] if conf[i] > MIN_CONF else None

    def process_person(self, frame, kpts, conf, person_id):
        def get(i):
            return self._get(kpts, conf, i)

        # Draw skeleton
        for a, b in SKELETON:
            if conf[a] > MIN_CONF and conf[b] > MIN_CONF:
                self.draw.line(frame, kpts[a], kpts[b])

        # Draw keypoints
        for i in range(len(kpts)):
            if conf[i] > MIN_CONF:
                self.draw.point(frame, kpts[i])

        # Joint angles
        joints = [
            (KP["l_sh"], KP["l_el"], KP["l_wr"]),
            (KP["r_sh"], KP["r_el"], KP["r_wr"]),
            (KP["l_hp"], KP["l_kn"], KP["l_an"]),
            (KP["r_hp"], KP["r_kn"], KP["r_an"])
        ]

        angles = {}
        for a, b, c in joints:
            key = f"{a}_{b}_{c}"
            if conf[a] > MIN_CONF and conf[b] > MIN_CONF and conf[c] > MIN_CONF:
                ang = Vec.angle(kpts[a], kpts[b], kpts[c])
                angles[key] = ang
                self.draw.arc(frame, kpts[a], kpts[b], kpts[c])
                cv2.putText(
                    frame,
                    f"{int(round(ang))}°",
                    tuple(np.round(kpts[b]).astype(int)),
                    cv2.FONT_HERSHEY_SIMPLEX,
                    0.5,
                    (255, 255, 255),
                    1,
                    cv2.LINE_AA
                )

        # COM / CoG approximation from hips + knees if available
        pts = []
        for key in ("l_hp", "r_hp", "l_kn", "r_kn"):
            p = get(KP[key])
            if p is not None:
                pts.append(p)

        com = np.mean(pts, axis=0) if len(pts) else None
        cog = com  # same approximation here

        if com is not None:
            com_xy = tuple(np.round(com).astype(int))
            cv2.circle(frame, com_xy, 6, (255, 0, 255), -1)
            cv2.putText(
                frame,
                f"COM/CoG {person_id}",
                com_xy,
                cv2.FONT_HERSHEY_SIMPLEX,
                0.5,
                (255, 0, 255),
                1,
                cv2.LINE_AA
            )

        la = get(KP["l_an"])
        ra = get(KP["r_an"])

        # Support line between ankles
        if la is not None and ra is not None:
            self.draw.line(frame, la, ra, (0, 200, 255), 2)

        left_force = None
        right_force = None
        balance_vector = None
        support_center = None

        # Left leg force vector
        if get(KP["l_kn"]) is not None and la is not None and get(KP["l_hp"]) is not None and com is not None:
            v, m = self.force.compute(get(KP["l_kn"]), la, get(KP["l_hp"]), com)
            self.draw.arrow(frame, la, v, (255, 0, 0), f"{int(round(m))}", scale=0.15)
            left_force = {"vector": [float(v[0]), float(v[1])], "mag": float(m)}

        # Right leg force vector
        if get(KP["r_kn"]) is not None and ra is not None and get(KP["r_hp"]) is not None and com is not None:
            v, m = self.force.compute(get(KP["r_kn"]), ra, get(KP["r_hp"]), com)
            self.draw.arrow(frame, ra, v, (255, 0, 0), f"{int(round(m))}", scale=0.15)
            right_force = {"vector": [float(v[0]), float(v[1])], "mag": float(m)}

        # Stability
        stable, score, support_center = self.stab.compute(com, la, ra, frame.shape[1])

        # Balance vector: from COM to support center
        if com is not None and support_center is not None:
            balance_vector = support_center - com
            self.draw.arrow(frame, com, balance_vector, (0, 255, 0) if stable else (0, 0, 255), "Balance", scale=1.0)

        # Stability label on video
        label = "STABLE" if stable else "UNSTABLE"
        label_color = (0, 255, 0) if stable else (0, 0, 255)

        label_pos = (30, 40 + 28 * person_id)
        if com is not None:
            label_pos = tuple(np.round(com + np.array([10, -10], dtype=np.float32)).astype(int))

        cv2.putText(
            frame,
            f"{label} ({score:.2f})",
            label_pos,
            cv2.FONT_HERSHEY_SIMPLEX,
            0.6,
            label_color,
            2,
            cv2.LINE_AA
        )

        return {
            "person_id": int(person_id),
            "com": [float(com[0]), float(com[1])] if com is not None else None,
            "cog": [float(cog[0]), float(cog[1])] if cog is not None else None,
            "support_center": [float(support_center[0]), float(support_center[1])] if support_center is not None else None,
            "left_ankle": [float(la[0]), float(la[1])] if la is not None else None,
            "right_ankle": [float(ra[0]), float(ra[1])] if ra is not None else None,
            "left_force": left_force,
            "right_force": right_force,
            "balance_vector": [float(balance_vector[0]), float(balance_vector[1])] if balance_vector is not None else None,
            "stability": {"stable": bool(stable), "score": float(score)},
            "angles": angles
        }

    def process_frame(self, frame):
        results = self.model(frame, verbose=False)

        frame_records = []
        if not results or len(results) == 0 or results[0].keypoints is None:
            cv2.putText(
                frame,
                "No person detected",
                (30, 40),
                cv2.FONT_HERSHEY_SIMPLEX,
                1,
                (0, 0, 255),
                2,
                cv2.LINE_AA
            )
            return frame, frame_records

        r = results[0]

        # Multi-person pose:
        # keypoints.xy -> (N, 17, 2)
        # keypoints.conf -> (N, 17)
        kpts_all = r.keypoints.xy.cpu().numpy()
        conf_all = r.keypoints.conf.cpu().numpy()

        if len(kpts_all) == 0:
            cv2.putText(
                frame,
                "No person detected",
                (30, 40),
                cv2.FONT_HERSHEY_SIMPLEX,
                1,
                (0, 0, 255),
                2,
                cv2.LINE_AA
            )
            return frame, frame_records

        for pid, (kpts, conf) in enumerate(zip(kpts_all, conf_all)):
            record = self.process_person(frame, kpts, conf, pid)
            frame_records.append(record)

            # Person label at nose if available, otherwise first valid point
            if conf[KP["nose"]] > MIN_CONF:
                name_xy = tuple(np.round(kpts[KP["nose"]]).astype(int))
            else:
                valid = kpts[conf > MIN_CONF]
                if len(valid) > 0:
                    name_xy = tuple(np.round(valid[0]).astype(int))
                else:
                    name_xy = (30, 80 + 25 * pid)

            cv2.putText(
                frame,
                f"Person {pid}",
                name_xy,
                cv2.FONT_HERSHEY_SIMPLEX,
                0.6,
                (0, 255, 255),
                2,
                cv2.LINE_AA
            )

        return frame, frame_records

# ---------------------------
# RUN
# ---------------------------
cap = cv2.VideoCapture(INPUT_VIDEO)
if not cap.isOpened():
    raise FileNotFoundError(f"Could not open input video: {INPUT_VIDEO}")

w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
fps = cap.get(cv2.CAP_PROP_FPS)
if fps is None or fps <= 1e-6:
    fps = 30.0

out = cv2.VideoWriter(
    OUTPUT_VIDEO,
    cv2.VideoWriter_fourcc(*"mp4v"),
    fps,
    (w, h)
)

engine = Engine()
timeline = []
frame_idx = 0

while True:
    ret, frame = cap.read()
    if not ret:
        break

    annotated, people = engine.process_frame(frame)
    out.write(annotated)

    timeline.append({
        "frame": int(frame_idx),
        "time": round(frame_idx / fps, 4),
        "num_persons": int(len(people)),
        "persons": people
    })

    frame_idx += 1

cap.release()
out.release()

with open(OUTPUT_JSON, "w") as f:
    json.dump(timeline, f, indent=2)

print(f"Done. Saved video: {OUTPUT_VIDEO}")
print(f"Done. Saved JSON: {OUTPUT_JSON}")