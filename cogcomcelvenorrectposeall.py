class Force:
    def compute(self, knee, ankle, hip, com):
        knee = np.asarray(knee, dtype=np.float32)
        ankle = np.asarray(ankle, dtype=np.float32)
        hip = np.asarray(hip, dtype=np.float32)
        com = np.asarray(com, dtype=np.float32)

        shank = ankle - knee
        normal = Vec.perp(Vec.unit(shank))
        knee_angle = Vec.angle(hip, knee, ankle)
        kf = np.cos(np.radians(knee_angle))

        offset = Vec.unit(com - ankle)
        of = float(np.dot(offset, normal))

        mag = 100 + 120 * abs(kf) + 80 * abs(of)
        vertical = np.array([0, -1], dtype=np.float32)
        direction = Vec.unit(0.6 * normal + 0.4 * vertical)
        return direction * mag, float(mag)

class Stability:
    def compute(self, com, la, ra, width):
        if com is None or (la is None and ra is None):
            return False, 0.0, None

        feet = [p for p in (la, ra) if p is not None]
        xs = [float(p[0]) for p in feet]
        xmin, xmax = min(xs), max(xs)

        # Expand support region a bit
        margin = 0.12 * width
        xmin -= margin
        xmax += margin

        dist = 0.0
        if com[0] < xmin:
            dist = xmin - com[0]
        elif com[0] > xmax:
            dist = com[0] - xmax

        score = float(np.exp(-dist / 40.0))
        stable = score > 0.5

        support_center = np.array([(xmin + xmax) / 2.0, float(np.mean([la[1], ra[1]]) if la is not None and ra is not None else feet[0][1])], dtype=np.float32)
        return stable, score, support_center

